void clear_item(Map *map, int item){
  bool find = false;
  for(int row = 0; row < mapHeight && find == false; row++){
    for(int column = 0; column < mapHeight && find == false; column++){
      if((*map)->matriz[row][column] == item){
        (*map)->matriz[row][column] = 0;
        find = true;
      }
    }
  }
}

int get_value_of(Map map, int x, int y){
  return map->matriz[x][y];
}

