#include "maps.h"

struct map{
  int matriz[mapWidth][mapHeight];
};

Map init_map(){
  Map map;
  map = malloc(sizeof(struct map));
  generate_map(&map, main_room);
  return map;
}

void generate_map(Map* map, int room[][mapHeight]){
  for(int row = 0; row < mapHeight; row++){
    for(int column = 0; column < mapHeight; column++){
      (*map)->matriz[row][column] = room[row][column];
    }
  }
}

