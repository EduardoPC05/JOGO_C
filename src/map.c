#include "../lib/map.h"

struct map{
    int matriz[MAPWIDTH][MAPHEIGHT];
    int actual_room;
};

void init_map(Map *map){
    Map m;
    m = malloc(sizeof(struct map));
    generate_map(&m, main_room);
    m->actual_room = 1;

    if(map != NULL){
        *map = m;
    }else{
        printf("Error, & is NULL - Map");
    }
}

void update_actual_room(Map map, int room){
    if(room > 0 && room < 6){
        map->actual_room = room;
    }
}

int get_actual_room(Map map){
    return map->actual_room;
}

void generate_map(Map *map, int room[][MAPHEIGHT]){
    for (int row = 0; row < MAPHEIGHT; row++){
        for (int column = 0; column < MAPHEIGHT; column++){
            (*map)->matriz[row][column] = room[row][column];
        }
    }
}

void clear_item(Map map){
    bool find = false;
    for (int row = 0; row < MAPHEIGHT && find == false; row++){
        for (int column = 0; column < MAPHEIGHT && find == false; column++){
            if (map->matriz[row][column] == 9){
                map->matriz[row][column] = 10;
                find = true;
                //Cleaning original map
                switch (map->actual_room){
                case 1:
                    main_room[row][column] = 10;
                    break;
                case 2:
                    second_room[row][column] = 10;
                    break;
                case 3:
                    third_room[row][column] = 10;
                    break;
                case 4:
                    fourth_room[row][column] = 10;
                    break;
                case 5:
                    fifth_room[row][column] = 10;
                    break;
                }
            }
        }
    }
}

void restart_itens(){
    for (int row = 0; row < MAPHEIGHT; row++){
        for (int column = 0; column < MAPHEIGHT; column++){
            if (main_room[row][column] == 10){
                main_room[row][column] = 9;
            }
            if (second_room[row][column] == 10){
                second_room[row][column] = 9;
            }
            if (third_room[row][column] == 10){
                third_room[row][column] = 9;
            }
            if (fourth_room[row][column] == 10){
                fourth_room[row][column] = 9;
            }
            if (fifth_room[row][column] == 10){
                fifth_room[row][column] = 9;
            }
        }
    }
}

int on_map(Map map, int x, int y){
    return map->matriz[x][y];
}

void close_map(Map *map){
    if(map != NULL){
       free(*map); 
    }
}

